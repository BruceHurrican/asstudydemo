package com.bruceutils.utils;

import android.content.Context;
import android.util.TypedValue;

/**
 * dp sp px 转换工具类
 * Created by BruceHurrican on 2016/2/7.
 */
public final class DpSpPxUtil {
    /**
     * 将 px值转换成 dip 或 dp 值,保证尺寸大小不变
     *
     * @param context
     * @param pxValue
     * @return
     */
    public static int px2dp(Context context, float pxValue) {
        final float scale = context.getResources().getDisplayMetrics().density;
        return (int) (pxValue / scale + 0.5f);
    }

    /**
     * 将 dip 或 dp 值转换成 px 值, 保证尺寸大小不变
     *
     * @param context
     * @param dpValue
     * @return
     */
    public static int dp2px(Context context, float dpValue) {
        final float scale = context.getResources().getDisplayMetrics().density;
        return (int) (dpValue * scale + 0.5f);
    }

    /**
     * 系统自带转换方法和上述方法相同
     *
     * @param context
     * @param dpValue
     * @return
     */
    public static int dp2px2(Context context, int dpValue) {
        return (int) TypedValue.applyDimension(TypedValue.COMPLEX_UNIT_DIP, dpValue, context.getResources().getDisplayMetrics());
    }

    /**
     * 将 px 值转换为 sp 值, 保证文字大小不变
     *
     * @param context
     * @param pxValue
     * @return
     */
    public static int px2sp(Context context, float pxValue) {
        final float fontScale = context.getResources().getDisplayMetrics().scaledDensity;
        return (int) (pxValue / fontScale + 0.5f);
    }


    /**
     * 将 sp 值转换为 px 值,保证文字大小不变
     *
     * @param context
     * @param spValue
     * @return
     */
    public static int sp2px(Context context, float spValue) {
        final float fontScale = context.getResources().getDisplayMetrics().scaledDensity;
        return (int) (spValue * fontScale + 0.5f);
    }

    /**
     * 系统自带转换方法和上述方法相同
     *
     * @param context
     * @param spValue
     * @return
     */
    public static int sp2px2(Context context, float spValue) {
        return (int) TypedValue.applyDimension(TypedValue.COMPLEX_UNIT_DIP, spValue, context.getResources().getDisplayMetrics());
    }
}
